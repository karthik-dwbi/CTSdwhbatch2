SELECT A.EMPLOYEE_ID,A.FIRST_NAME,A.LAST_NAME,B.FIRST_NAME AS MANAGER_NAME
FROM EMPLOYEES 
A LEFT OUTER JOIN EMPLOYEES B ON A.MANAGER_ID=B.MANAGER_ID
ORDER BY EMPLOYEE_ID;


