SELECT DEPARTMENT_NAME,COUNT(EMPLOYEE_ID) AS NO_OF_EMPLOYEES 
FROM EMPLOYEES E
JOIN DEPARTMENTS D ON
E.DEPARTMENT_ID=D.DEPARTMENT_ID
GROUP BY DEPARTMENT_NAME
HAVING COUNT(EMPLOYEE_ID)>=20;



