SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,(FIRST_NAME||''||LAST_NAME)
FULL_NAME,SALARY,(SALARY*0.1)NEW_SALARY,(SALARY-(SALARY*0.1))
DIFFERENCE FROM EMPLOYEES;



