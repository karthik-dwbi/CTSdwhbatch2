SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY
FROM EMPLOYEES E
WHERE SALARY>(SELECT AVG(SALARY)FROM EMPLOYEES X
WHERE E.DEPARTMENT_ID=X.DEPARTMENT_ID
GROUP BY E.EMPLOYEE_ID) 


